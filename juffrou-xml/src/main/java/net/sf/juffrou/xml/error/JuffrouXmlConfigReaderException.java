package net.sf.juffrou.xml.error;

public class JuffrouXmlConfigReaderException extends RuntimeException {

	/**
	 * 
	 */
	private static final long serialVersionUID = -771016197131250008L;

	public JuffrouXmlConfigReaderException() {
		super();
	}

	public JuffrouXmlConfigReaderException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

	public JuffrouXmlConfigReaderException(String arg0) {
		super(arg0);
	}

	public JuffrouXmlConfigReaderException(Throwable arg0) {
		super(arg0);
	}

	
}
