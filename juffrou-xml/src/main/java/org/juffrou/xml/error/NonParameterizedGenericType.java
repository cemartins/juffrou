package org.juffrou.xml.error;

import org.juffrou.xml.JuffrouXmlException;

public class NonParameterizedGenericType extends JuffrouXmlException {

	/**
	 * 
	 */
	private static final long serialVersionUID = -3824689879259947860L;
	

	public NonParameterizedGenericType() {
		super();
	}

	public NonParameterizedGenericType(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

	public NonParameterizedGenericType(String arg0) {
		super(arg0);
	}

	public NonParameterizedGenericType(Throwable arg0) {
		super(arg0);
	}
	
}
